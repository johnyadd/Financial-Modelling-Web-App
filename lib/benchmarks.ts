export function getBenchmarks(
  industry: string,
  subSector: string
): SectorBenchmarks | null {
  return BENCHMARKS[industry]?.[subSector] ?? null
}

export function getMetricStatus(
  value: number,
  range: BenchmarkRange
): "low" | "normal" | "high" | "outlier-low" | "outlier-high" {
  const { min, typical, max } = range
  const buffer = (max - min) * 0.1
  if (value < min - buffer) return "outlier-low"
  if (value > max + buffer) return "outlier-high"
  if (value < min) return "low"
  if (value > max) return "high"
  return "normal"
}

export function getBenchmarkWarning(
  metricLabel: string,
  value: number,
  range: BenchmarkRange
): string | null {
  const status = getMetricStatus(value, range)
  if (status === "normal" || status === "low" || status === "high") return null
  if (status === "outlier-low") {
    return `${metricLabel} of ${value}${range.unit} is unusually low for this sector (typical range: ${range.min}–${range.max}${range.unit}). Please double-check this figure.`
  }
  if (status === "outlier-high") {
    return `${metricLabel} of ${value}${range.unit} is unusually high for this sector (typical range: ${range.min}–${range.max}${range.unit}). Please double-check this figure.`
  }
  return null
}

export function getPreFillValue(
  industry: string,
  subSector: string,
  metric: keyof SectorBenchmarks
): number | null {
  const benchmarks = getBenchmarks(industry, subSector)
  if (!benchmarks) return null
  return benchmarks[metric].typical
}

