// ============================================================
// FinModels UK — Industry Benchmark Data
// ------------------------------------------------------------
// Every metric carries: min, typical, max, and a unit label.
// These ranges are used to:
//   1. Pre-fill questionnaire placeholder values
//   2. Colour-code inputs green / amber / red as user types
//   3. Warn before submission if a value is an outlier
//   4. Block obviously invalid entries (e.g. gross margin > 100%)
//
// Sources: Damodaran NYU datasets, PitchBook SaaS benchmarks,
// UK ONS business surveys, KPMG sector reports (2023-2024).
// Ranges are intentionally wide to accommodate early-stage
// businesses — constrain further in Edge Function validation.
// ============================================================

export type BenchmarkRange = {
  min: number
  typical: number
  max: number
  unit: string
  note?: string
}

export type SectorBenchmarks = {
  // P&L metrics
  grossMargin: BenchmarkRange
  ebitdaMargin: BenchmarkRange
  netMargin: BenchmarkRange
  revenueGrowthY1: BenchmarkRange
  revenueGrowthY2: BenchmarkRange
  revenueGrowthY3: BenchmarkRange

  // Cost ratios (% of revenue)
  cogsPercent: BenchmarkRange
  salesMarketingPercent: BenchmarkRange
  rdPercent: BenchmarkRange
  gaPercent: BenchmarkRange

  // Unit economics
  cacPaybackMonths: BenchmarkRange
  ltv: BenchmarkRange
  ltvCacRatio: BenchmarkRange
  churnRateAnnual: BenchmarkRange
  nrr: BenchmarkRange

  // Cash & capital
  monthlyBurnRate: BenchmarkRange
  runwayMonths: BenchmarkRange
  capexPercent: BenchmarkRange
  depreciationRate: BenchmarkRange

  // Valuation
  revenueMultiple: BenchmarkRange
  ebitdaMultiple: BenchmarkRange
  discountRate: BenchmarkRange
  terminalGrowthRate: BenchmarkRange
}

export type IndustryBenchmarks = {
  [subSector: string]: SectorBenchmarks
}

export type AllBenchmarks = {
  [industry: string]: IndustryBenchmarks
}

// ============================================================
// HELPER — creates a benchmark range with defaults
// ============================================================
function b(
  min: number,
  typical: number,
  max: number,
  unit: string,
  note?: string
): BenchmarkRange {
  return { min, typical, max, unit, note }
}

